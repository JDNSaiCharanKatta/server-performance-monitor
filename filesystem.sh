#!/usr/bin/env bash

###############################################################################
# File        : filesystem.sh
# Description : Filesystem Monitoring Module
# Version     : 2.0
###############################################################################

set -Eeuo pipefail

###############################################################################
# Mounted File Systems
###############################################################################

mounted_filesystems() {

    echo
    echo "Mounted File Systems"
    echo "--------------------------------------------------------------"

    printf "%-25s %-10s %-10s %-10s %-8s %-12s\n" \
        "Mount Point" "Size" "Used" "Avail" "Use%" "Type"

    df -Th | awk 'NR>1{
        printf "%-25s %-10s %-10s %-10s %-8s %-12s\n",
        $7,$3,$4,$5,$6,$2
    }'

}

###############################################################################
# Filesystem Usage
###############################################################################

filesystem_usage() {

    echo
    echo "Filesystem Usage"
    echo "--------------------------------------------------------------"

    df -hP

}

###############################################################################
# Inode Usage
###############################################################################

inode_usage() {

    echo
    echo "Inode Usage"
    echo "--------------------------------------------------------------"

    df -ihP

}

###############################################################################
# Read Only Mounts
###############################################################################

readonly_mounts() {

    echo
    echo "Read Only Mount Points"
    echo "--------------------------------------------------------------"

    mount | grep "(ro," || echo "No Read Only Mounts"

}

###############################################################################
# Largest Directories
###############################################################################

largest_directories() {

    echo
    echo "Top 10 Largest Directories (/)"
    echo "--------------------------------------------------------------"

    du -xh / --max-depth=1 2>/dev/null \
    | sort -hr \
    | head -10

}

###############################################################################
# Largest Files
###############################################################################

largest_files() {

    echo
    echo "Top 10 Largest Files"
    echo "--------------------------------------------------------------"

    find / -xdev -type f -exec du -h {} + 2>/dev/null \
    | sort -hr \
    | head -10

}

###############################################################################
# Open File Descriptors
###############################################################################

open_files() {

    echo
    echo "Open File Descriptors"
    echo "--------------------------------------------------------------"

    if command -v lsof >/dev/null 2>&1
    then
        echo "Total Open Files : $(lsof 2>/dev/null | wc -l)"
    else
        echo "lsof not installed."
    fi

}

###############################################################################
# Disk Health
###############################################################################

filesystem_health() {

    echo
    echo "Filesystem Health"
    echo "--------------------------------------------------------------"

    df -P | awk 'NR>1{

        use=$5
        gsub("%","",use)

        status="OK"

        if(use>=90)
            status="CRITICAL"
        else if(use>=80)
            status="WARNING"

        printf "%-25s %-10s %-15s\n",$6,$5,status

    }'

}

###############################################################################
# Mount Options
###############################################################################

mount_options() {

    echo
    echo "Mount Options"
    echo "--------------------------------------------------------------"

    mount | head -20

}

###############################################################################
# Recently Modified Files
###############################################################################

recent_files() {

    echo
    echo "Recently Modified Files (/var)"
    echo "--------------------------------------------------------------"

    find /var -type f -mtime -1 2>/dev/null | head -20

}

###############################################################################
# Filesystem Report
###############################################################################

filesystem_report() {

    print_section "FILESYSTEM INFORMATION"

    mounted_filesystems

    filesystem_usage

    inode_usage

    filesystem_health

    open_files

    readonly_mounts

}

###############################################################################
# End
